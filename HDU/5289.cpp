int t,n,m;
#define MAX 100005
int arr[MAX];


pii tree[MAX*3];
pii call(pii a,pii b)
{
    pii tmp;
    tmp.ff=max(a.ff,b.ff);
    tmp.ss=min(a.ss,b.ss);
    return tmp;
}

void init(int node,int b,int e)
{
	if(b==e)
	{
		tree[node].ff=arr[b];
		tree[node].ss=arr[b];
		return;
	}
	int Left=node*2;
	int Right=node*2+1;
	int mid=(b+e)/2;
	init(Left,b,mid);
	init(Right,mid+1,e);
	tree[node]=call(tree[Left],tree[Right]);
}
pii query(int node,int b,int e,int i,int j)
{
	if (i > e || j < b)	return pii(-INF,INF);
	if (b >= i && e <= j) return tree[node];
	int Left=node*2;
	int Right=node*2+1;
	int mid=(b+e)/2;
	pii p1=query(Left,b,mid,i,j);
	pii p2=query(Right,mid+1,e,i,j);
	return call(p1,p2);
}


