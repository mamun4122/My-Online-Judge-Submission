int t,n,m;
int dp[105][105][105];
int mark[105][105][105],ci;
pii arr[105];
int call(int pos,int frst,int scnd,int ans)
{
    if(frst==0&&scnd==0)return 1;
    if(pos==n)return 0;
    int &ret=dp[pos][frst][scnd];
    int &col=mark[pos][frst][scnd];
    if(col==ci)return ret;
    col=ci;
    for(int i=0;i<=frst && arr[pos].ff*i<=ans;i++)
    {
        ///frst ta loop die ber korlam ans scnd ta k baki seconds gula dilam
        int val=ans-arr[pos].ff*i;
        val/=arr[pos].ss;
        val=min(val,scnd);
        if(call(pos+1,frst-i,scnd-val,ans))return ret=1;
    }
    return ret=0;
}


